#include <libft.h>

t_ptr	ft_bzero(t_ptr ptr, t_size size)
{
	t_size	i;
	t_size	x;

	i = 0;
	while (i < size % 8)
		((t_u8 *)ptr)[i++] = 0;
	x = 0;
	while (x < size / 8)
		((t_u64 *)((t_u8 *)ptr + i))[x++] = 0;
	return (ptr);
}
